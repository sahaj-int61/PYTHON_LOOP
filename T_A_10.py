"""
Display a message “Done” after successful execution of for loop.
Date: 21/12/23
"""
# Initialize variables
num = 0

# Start the for loop to iterate 5 times
for i in range(5):
    # Print the current number
    print(num)
    # Increment the number by 1 for the next iteration
    num += 1

# After the successful execution of the for loop, print the "Done" message
print('Done')